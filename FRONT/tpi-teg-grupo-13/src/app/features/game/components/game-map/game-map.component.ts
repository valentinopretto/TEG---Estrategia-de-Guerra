  private showTooltip(territoryId: string, event: MouseEvent) {
    const territory = this.territories.find(t => t.id === territoryId);
    if (!territory) return;

    // Get territory data from gameState using normalized names
    let ownerName: string | null = null;
    let armies: number = 0;

    if (this.gameState?.territories) {
      const normalizedTerritoryName = this.normalizeTerritoryName(territory.name);

      for (const territoryData of Object.values(this.gameState.territories)) {
        const territoryDataNormalizedName = this.normalizeTerritoryName(territoryData.name);
        if (territoryDataNormalizedName === normalizedTerritoryName) {
          ownerName = territoryData.ownerName;
          armies = territoryData.armies || 0;
          break;
        }
      }
    }

    // Get continent
    const continent = this.continentMapping[territoryId] || 'Desconocido';

    this.tooltipData = {
      territoryName: territory.name,
      ownerName: ownerName,
      armies: armies,
      continent: continent
    };

    // Position tooltip near mouse cursor
    this.tooltipPosition = {
      x: event.clientX + 10,
      y: event.clientY - 10
    };
  }

  private hideTooltip() {
    this.tooltipData = null;
  }

  getTerritoryColor(TerritoryName?: string): string {
    if (!TerritoryName || !this.gameState?.territories) return 'blue';

    // Buscar el territorio por nombre usando normalizaci√≥n
    let ownerName: string | null = null;
    const normalizedTerritoryName = this.normalizeTerritoryName(TerritoryName);

    for (const territory of Object.values(this.gameState.territories)) {
      const territoryNormalizedName = this.normalizeTerritoryName(territory.name);
      if (territoryNormalizedName === normalizedTerritoryName) {
        ownerName = territory.ownerName;
        break;
      }
    }

    // Buscar el jugador due√±o
    if (this.players != null && ownerName != null) {
      const player = this.players.find(p => p.displayName === ownerName);
      if (!player) return 'white';

      // Devolver el color
      switch (player.color) {
        case 'RED': return 'rgba(224,2,2,0.84)';
        case 'BLUE': return 'rgba(2,28,197,0.84)';
        case 'GREEN': return 'rgba(30,110,3,0.91)';
        case 'YELLOW': return 'yellow';
        case 'BLACK': return 'rgba(10,10,10,0.96)';
        case 'PURPLE': return 'purple';
        default: return 'white';
      }
    }

    return 'white';
  }



