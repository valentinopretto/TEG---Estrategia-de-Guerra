  /**
   * Gets the troop count for a territory from gameState
   */
  getTerritoryTroops(territoryName: string): number {
    if (!this.gameState?.territories) return 0;

    // Normalize the territory name (remove accents, convert to lowercase)
    const normalizedName = this.normalizeTerritoryName(territoryName);

    // Search for territory by name in gameState
    for (const territory of Object.values(this.gameState.territories)) {
      const territoryNormalizedName = this.normalizeTerritoryName(territory.name);
      if (territoryNormalizedName === normalizedName) {
        //console.log(`Found troops for ${territoryName}: ${territory.armies}`);
        return territory.armies || 0;
      }
    }
    return 0;
  }

  /**
   * Normalizes territory names by removing accents and converting to lowercase
   */
  private normalizeTerritoryName(name: string): string {
    return name
      .toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '') // Remove accents
      .trim();
  }

  /**
   * Gets the troop count for a troop display element
   */
  getTroopCount(troop: {id: string, name: string, path: string, troops: number | null}): number {
    return this.getTerritoryTroops(troop.name);
  }

